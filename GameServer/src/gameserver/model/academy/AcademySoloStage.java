package gameserver.model.academy;

import java.util.ArrayList;
import java.util.List;

public class AcademySoloStage
{

    public AcademySoloStage()
    {
    }

    public List getAcademyRound()
    {
        if(academySoloRound == null)
            academySoloRound = new ArrayList();
        return academySoloRound;
    }

    public int getStage()
    {
        return id;
    }

    public int size()
    {
        return getAcademyRound().size();
    }

    protected List academySoloRound;
    protected int id;
}
